    private void saveResultToDbBMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveResultToDbBMouseClicked
        saveResultBMouseClicked(evt);
        
        SimpleDateFormat fmt = new SimpleDateFormat("MMM d,yyyy hh:mm:ss");
        String actionTime = fmt.format(Helper.actionDate);
        
        ArrayList<String> param=new ArrayList<String>();
        String insertRecordQuery = "Insert into TESTS(CNIC, IMAGES_PATH,PIECES,TEST_DATE) "
                                    + "values(?,?,?,Parse (? as DateTime2))";
        param.add(Helper.cnic);
        param.add(Helper.imgFolderPath);
        param.add(Integer.toString(Helper.pieces) );
        param.add(actionTime);
        
        if(DBHandler.runUpdate(insertRecordQuery,param))
            JOptionPane.showMessageDialog(null, "Reconstruction Recorded Succesfully","Message",JOptionPane.INFORMATION_MESSAGE);
        else
            JOptionPane.showMessageDialog(rootPane, "Reconstruction could not Recorded","Error",JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_saveResultToDbBMouseClicked

