    private void cleanMatlabFolder() {
        try {
            File filesToDelete = new File(Helper.docsPath+"MatlabCode\\");
            File [] files = filesToDelete.listFiles();
            
            for (File file : files) {
                if(file.getName().endsWith(".jpg"))
                    Files.delete(file.toPath());
            }
            File sampleFile = new File(Helper.docsPath+"sample.jpg");
            Files.delete(sampleFile.toPath());
        } catch (IOException ex) {
            Logger.getLogger(ResultForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
